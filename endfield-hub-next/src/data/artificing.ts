// ============ Helper Functions ============

export function getGearByPart(partType: PartType): ArtificingGear[] {
  return GEAR_ARTIFICING_DATA.filter(g => g.partType === partType);
}

export function getMatchTier(qualityPct: number): MatchTier {
  if (qualityPct >= 100) return 'Best Pick';
  if (qualityPct >= 70) return 'Good';
  if (qualityPct >= 45) return 'Partial';
  return 'Standard';
}

interface FodderRecommendation {
  gear: ArtificingGear;
  substat: GearSubstat;
  matchTier: MatchTier;
}

export function getFodderRecommendations(
  targetGearId: string,
  targetStatKey: string,
  currentLevel: number
): FodderRecommendation[] {
  const targetGear = GEAR_ARTIFICING_DATA.find(g => g.id === targetGearId);
  if (!targetGear) return [];

  const targetStat = targetGear.substats.find(s => s.statKey === targetStatKey);
  if (!targetStat) return [];

  // Find all gear of the same part type
  const samePart = getGearByPart(targetGear.partType);

  // Collect all fodder options for this stat
  const fodderOptions: FodderRecommendation[] = [];

  samePart.forEach(gear => {
    gear.substats.forEach(substat => {
      if (substat.statKey === targetStatKey) {
        fodderOptions.push({
          gear,
          substat,
          matchTier: getMatchTier(substat.qualityPct),
        });
      }
    });
  });

  // Sort by quality percentage (descending)
  fodderOptions.sort((a, b) => b.substat.qualityPct - a.substat.qualityPct);

  return fodderOptions;
}

export function getStatPriority(partType: PartType): string[] {
  // General recommendation: Main attribute → Special stat → Secondary attribute
  if (partType === 'Body') {
    return [
      '1. Main Attribute (Str/Agi/Int/Will) - Direct ATK increase',
      '2. Special Stat (DMG bonus, Crit, etc.) - Multiplier effect',
      '3. Secondary Attribute - Least impactful',
    ];
  }
  if (partType === 'Hand') {
    return [
      '1. Main Attribute (Str/Agi/Int/Will) - Direct ATK increase',
      '2. Special Stat (Skill DMG, Crit, etc.) - Multiplier effect',
      '3. Secondary Attribute - Least impactful',
    ];
  }
  if (partType === 'EDC') {
    return [
      '1. Main Attribute (Str/Agi/Int/Will) - Direct ATK increase (Redeemer gear has longer pity)',
      '2. Special Stat (Skill DMG, DMG bonus, etc.) - Build-defining',
      '3. Secondary Attribute (if present) - Minor impact',
    ];
  }
  return [];
}

