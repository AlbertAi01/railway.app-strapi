// Helper: Get guarantee times based on stat and slot
function getGuaranteeTimes(statKey: string, partType: PartType, isStandalone: boolean): number[] {
  // Standalone EDC primary attributes have longer guarantee times
  if (partType === 'EDC' && isStandalone && ['Strength', 'Agility', 'Intellect', 'Will'].includes(statKey)) {
    return [12, 24, 40];
  }
  // Default guarantee times for all other stats
  return [6, 12, 20];
}

// Helper: Determine if a stat is a primary attribute
function isPrimaryAttribute(statKey: string): boolean {
  return ['Strength', 'Agility', 'Intellect', 'Will'].includes(statKey);
}

// Helper: Get max value for a stat on a given part type
function getMaxValue(statKey: string, partType: PartType, isStandalone: boolean): number {
  if (isPrimaryAttribute(statKey)) {
    if (partType === 'Body') return MAX_VALUES.Body.primary;
    if (partType === 'Hand') return MAX_VALUES.Hand.primary;
    if (partType === 'EDC') return isStandalone ? MAX_VALUES.EDC.primary : MAX_VALUES.EDC.primarySet;
  }

  // Special stats
  const maxTable = partType === 'Body' ? MAX_VALUES.Body.special :
                   partType === 'Hand' ? MAX_VALUES.Hand.special :
                   MAX_VALUES.EDC.special;

  return (maxTable as any)[statKey] || 100; // fallback
}

// Parse numeric value from display string (handles "87", "+87", "20.7%", "-17.1%")
function parseStatValue(valueStr: string): number {
  const cleaned = valueStr.replace(/[+%]/g, '');
  return Math.abs(parseFloat(cleaned));
}

