/**
 * Gear Artificing Solver Data
 * Based on EndfieldTools.dev mechanics + Reddit community research
 *
 * Core mechanics:
 * - Gear has 3 slots: Body (Armor), Hand (Gloves), EDC (Accessory/Kit)
 * - Each piece has 2-3 substats that can be artificed L0 → L1 → L2 → L3
 * - Sacrificing a FODDER gear piece of the same slot type increases a stat
 * - Quality Chance = (fodder's stat value / max possible value) × 100
 * - Guarantee times (pity): varies by stat type
 */

export type PartType = 'Body' | 'Hand' | 'EDC';
export type MatchTier = 'Best Pick' | 'Good' | 'Partial' | 'Standard';

export interface GearSubstat {
  statKey: string;
  displayName: string;
  value: string; // Base value
  valueByLevel: string[]; // [L0, L1, L2, L3]
  guaranteeTimes: number[]; // [L0→1, L1→2, L2→3]
  maxScore: number; // Maximum possible value for this stat/slot combo
  qualityPct: number; // This gear's value as % of max
  isTop: boolean; // Is this the best possible fodder for this stat?
}

export interface ArtificingGear {
  id: string;
  name: string;
  setName: string | null;
  partType: PartType;
  substats: GearSubstat[];
}

